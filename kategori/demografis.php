    <h2>Kondisi Ekonomi</h2>
    <h3>a. Potensi Unggulan Desa</h3>
    <p>1) Potensi Lahan dan Usaha Pertanian</p>
    <p>Pengembangan usaha pertanian di Desa Cangkring ditunjang oleh ketersediaan lahan pertanian yang subur dan sarana irigasi yang tertata. Luas panen padi pada tahun 2023 mencapai 160 hektar dengan produksi 6 hingga 8 ton/ha.</p>
    <p>2) Tanaman palawija merupakan tanaman potensial untuk dikembangkan di Desa Cangkring.</p>

    <h3>b. Pertumbuhan Ekonomi</h3>
    <p>Pertumbuhan ekonomi Desa Cangkring tidak bisa dilepaskan dari pertumbuhan ekonomi Kabupaten Demak secara keseluruhan. Produk Domestik Regional Bruto (PDRB) Kabupaten Demak mengalami peningkatan setiap tahunnya, yang berpengaruh positif terhadap pertumbuhan ekonomi Desa Cangkring.</p>
    <p>Secara geografis, Desa Cangkring termasuk dalam Kecamatan Karanganyar Kabupaten Demak dan memiliki batas-batas wilayah sebagai berikut:</p>
    <ul>
        <li>Sebelah Utara: Desa Wonorejo</li>
        <li>Sebelah Selatan: Desa Tuwang</li>
        <li>Sebelah Timur: Desa Ngemplik/Ds Tuwang</li>
        <li>Sebelah Barat: Desa Cangkring Rembang</li>
    </ul>
    <p>Desa Cangkring mengalami dua musim, yaitu musim kemarau antara April hingga September dan musim penghujan antara Oktober hingga Maret. Rata-rata curah hujan di Desa Cangkring adalah 227,3 mm, dengan suhu antara 26°C hingga 29°C dan kelembapan udara sekitar 77,4% hingga 87,3%.</p>
    <p>Desa Cangkring memiliki potensi kekayaan alam yang bervariatif, terutama di sektor pertanian yang menjadi sumber penghasilan utama sekitar 40% penduduk desa.</p>
    <p>Desa Cangkring memiliki 5 Rukun Warga dan 37 Rukun Tetangga. Desa ini juga strategis karena berada dekat dengan Kota Kabupaten Kudus, sehingga banyak penduduk yang bekerja di luar desa.</p>
