        <h1>Sejarah Desa Cangkring</h1>

        <h2>Asal Usul Nama Desa</h2>
        <p>Asal usul penamaan Desa Cangkring diyakini diambil dari jenis pohon bernama Cangkring yang dulu pernah ada di desa ini. Menurut cerita tetua desa, Cangkring merupakan jenis pohon Dadap yang memiliki duri. Cangkring (Erythrina fusca) adalah spesies pohon berbunga dalam keluarga kacang-kacangan, Fabaceae. Di beberapa tempat, pohon ini dikenal dengan nama lain seperti Purple Coraltree, Gallito, Bois Immortelle, Bucayo, dan beberapa nama lainnya. Pohon ini tumbuh di sepanjang sungai dan pantai di daratan Asia beriklim tropis, Oceania, Kepulauan Mascarene, Madagaskar, Afrika, dan Neotropik.</p>
        <p>Jenis pohon ini adalah pohon gugur dengan kulit berduri dan bunga oranye terang. Polong legumnya mencapai panjang hingga 20 cm dan mengandung biji coklat gelap. Biji pohon ini dapat mengapung di laut dan tersebar di sepanjang pantai. Pohon ini sangat adaptif terhadap lingkungan pesisir yang kadang tergenang banjir dan lingkungan air-tanah bersalinitas tinggi.</p>
        
        <h2>Penggunaan dan Manfaat Pohon Cangkring</h2>
        <p>Seperti halnya spesies lain dalam genus Erythrina, E. fusca mengandung alkaloid toksik yang digunakan secara luas sebagai obat namun beracun apabila digunakan dalam jumlah besar. Alkaloid yang paling umum adalah erythraline, yang digunakan sebagai nama genus. Tunas dan daun mudanya dapat dikonsumsi sebagai sayuran. Pohon ini banyak dibudidayakan sebagai ornamen penghias dan tanaman pagar. Pohon ini juga merupakan pohon rindang yang umum di perkebunan kakao, dan bunganya yang atraktif mengundang burung Kolibri untuk membantu proses penyerbukan.</p>
        
